% Дані поточного року 
x11 = 700; % Промисловість для промисловості 
x12 = 400; % Промисловість для с/г 
y1 = 600; % Промисловість для резерву 

x21 = 900; % С/г для промисловості 
x22 = 300; % С/г для с/г 
y2 = 550; % С/г для резерву 

% Обчислення повного випуску 
x1 = x11 + x12 + y1; 
x2 = x21 + x22 + y2; 

% Обчислення коефіцієнтів матеріальних витрат 
a11 = x11 / x1; 
a12 = x12 / x2; 
a21 = x21 / x1; 
a22 = x22 / x2; 

% Технологічна матриця 
A = [a11, a12; a21, a22]; 

% Новий прогнозований попит 
Y_new = [1100; 1300]; 

% Розв'язок системи (I - A)X = Y 
eyeMatrix = eye(2); % Одинична матриця 
X = (eyeMatrix - A) \ Y_new; 

% Відображення результату 
fprintf('Повний випуск промислової продукції: %.2f млн. грн.\n', X(1)); 
fprintf('Повний випуск сільськогосподарської продукції: %.2f млн. грн.\n', X(2)); 

% Побудова графіків 
categories = {'Поточний випуск', 'Необхідний випуск'}; 
industry_values = [x1, X(1)]; 
agriculture_values = [x2, X(2)]; 

figure; 
bar(categorical(categories), [industry_values; agriculture_values]') 
legend({'Промисловість', 'Сільське господарство'}) 
title('Порівняння поточного та необхідного випуску') 
xlabel('Стан') 
ylabel('Випуск (млн. грн.)') 

grid on; 

% Динаміка змін випуску 
figure; 
plot([x1, X(1)], '-o', 'LineWidth', 2); 
hold on; 
plot([x2, X(2)], '-s', 'LineWidth', 2); 
hold off; 
legend({'Промисловість', 'Сільське господарство'}, 'Location', 'Best'); 
title('Динаміка змін випуску продукції'); 
xlabel('Стан (1 - поточний, 2 - необхідний)'); 
ylabel('Випуск (млн. грн.)'); 
grid on;
